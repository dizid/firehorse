export function getArticleBySlug(slug: string): EncyclopediaArticle | undefined {
  return encyclopediaArticles.find(article => article.slug === slug)
}

export function getArticlesByCategory(category: EncyclopediaArticle['category'] | 'all'): EncyclopediaArticle[] {
  if (category === 'all') return encyclopediaArticles
  return encyclopediaArticles.filter(article => article.category === category)
}

export function getRelatedArticles(currentSlug: string, limit: number = 3): EncyclopediaArticle[] {
  const currentArticle = getArticleBySlug(currentSlug)
  if (!currentArticle) return []

  // Get articles from same category, excluding current article
  const sameCategory = encyclopediaArticles.filter(
    article => article.category === currentArticle.category && article.slug !== currentSlug
  )

  // If not enough from same category, add from other categories
  if (sameCategory.length < limit) {
    const others = encyclopediaArticles.filter(
      article => article.category !== currentArticle.category && article.slug !== currentSlug
    )
    return [...sameCategory, ...others].slice(0, limit)
  }

  return sameCategory.slice(0, limit)
}
